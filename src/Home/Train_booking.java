    private void bTOTALActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bTOTALActionPerformed
        // TODO add your handling code here:

        int price;
        int qty;
        int extra;
        int tot = 0;

        if(rbST.isSelected())
        {
            String odc=rbST.getText();
            price = Integer.parseInt(tfPRICE1.getText());
            qty = Integer.parseInt(spPPL.getValue().toString());
            tot=price*qty;
        }

        if(rbECO.isSelected())
        {
            String odc=rbECO.getText();
            price = Integer.parseInt(tfPRICE1.getText());
            extra = 3000;
            int sum = price+extra;
            qty = Integer.parseInt(spPPL.getValue().toString());
            tot=sum*qty;
        }

        if(rbPREM.isSelected())
        {
            String odc=rbPREM.getText();
            price = Integer.parseInt(tfPRICE1.getText());
            extra = 5000;
            int sum = price + extra;
            qty = Integer.parseInt(spPPL.getValue().toString());
            tot=sum*qty;
        }
        lblTOT.setText(String.valueOf(tot));

    }//GEN-LAST:event_bTOTALActionPerformed

